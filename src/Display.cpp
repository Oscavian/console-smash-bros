void Display::wait() {
    std::string anyKey;
    std::cout << "Press any key...";
    std::cin >> anyKey;
    std::cin.ignore();
    std::cin.clear();
    clearScreen();
}

void Display::clearScreen() {
#if defined(_WIN32)
    system("cls");
#elif defined(__linux__)
    system("clear");
#endif
}

